// ── Paragraph helpers ──────────────────────────────────────────────
const sp = (before = 0, after = 0) => ({ spacing: { before, after } });

function h1(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    children: [new TextRun({ text, bold: true, color: C.brand, size: 36 })],
    ...sp(360, 120),
  });
}
function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    children: [new TextRun({ text, bold: true, color: C.accent, size: 28 })],
    ...sp(280, 80),
  });
}
function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    children: [new TextRun({ text, bold: true, color: C.brand, size: 24 })],
    ...sp(200, 60),
  });
}
function h4(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_4,
    children: [new TextRun({ text, bold: true, color: C.midGray, size: 22 })],
    ...sp(160, 40),
  });
}

function body(text, opts = {}) {
  return new Paragraph({
    children: [new TextRun({ text, size: 22, color: "000000", ...opts })],
    ...sp(60, 60),
  });
}
function bodyBold(text) { return body(text, { bold: true }); }
function blank() { return new Paragraph({ children: [new TextRun("")], ...sp(0,0) }); }

function bullet(text, level = 0) {
  return new Paragraph({
    numbering: { reference: "bullets", level },
    children: [new TextRun({ text, size: 22 })],
    ...sp(40, 40),
  });
}
function numbered(text, level = 0) {
  return new Paragraph({
    numbering: { reference: "numbers", level },
    children: [new TextRun({ text, size: 22 })],
    ...sp(40, 40),
  });
}

// Mixed-run paragraph: array of {text, bold?, color?, code?}
function para(runs, opts = {}) {
  return new Paragraph({
    children: runs.map(r => new TextRun({
      text: r.text,
      bold: r.bold || false,
      color: r.color || "000000",
      font: r.code ? "Courier New" : "Arial",
      size: r.code ? 20 : 22,
      highlight: r.highlight || undefined,
      shading: r.code ? { type: ShadingType.CLEAR, fill: "E8E8E8" } : undefined,
    })),
    ...sp(60, 60),
    ...opts,
  });
}

// ── Code block ─────────────────────────────────────────────────────
function codeBlock(lines) {
  const rows = (Array.isArray(lines) ? lines : lines.split('\n')).map(line =>
    new TableRow({
      children: [new TableCell({
        borders: noBdr(),
        width: { size: 9360, type: WidthType.DXA },
        margins: { top: 0, bottom: 0, left: 160, right: 160 },
        shading: { fill: "1E1E2E", type: ShadingType.CLEAR },
        children: [new Paragraph({
          children: [new TextRun({
            text: line || " ",
            font: "Courier New",
            size: 18,
            color: "CDD6F4",
          })],
          spacing: { before: 0, after: 0 },
        })],
      })],
    })
  );
  return new Table({
    width: { size: 9360, type: WidthType.DXA },
    columnWidths: [9360],
    margins: { top: 120, bottom: 120 },
    rows,
  });
}

// ── Callout boxes ──────────────────────────────────────────────────
function callout(label, text, bgColor, labelColor) {
  return new Table({
    width: { size: 9360, type: WidthType.DXA },
    columnWidths: [9360],
    margins: { top: 80, bottom: 80 },
    rows: [new TableRow({ children: [new TableCell({
      borders: bdr(labelColor, 8),
      width: { size: 9360, type: WidthType.DXA },
      margins: { top: 100, bottom: 100, left: 180, right: 180 },
      shading: { fill: bgColor, type: ShadingType.CLEAR },
      children: [
        new Paragraph({ children: [new TextRun({ text: label, bold: true, color: labelColor, size: 20 })], spacing: { before: 0, after: 40 } }),
        new Paragraph({ children: [new TextRun({ text, size: 20, color: "000000" })], spacing: { before: 0, after: 0 } }),
      ],
    })] })],
  });
}
