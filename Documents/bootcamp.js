// ── Simple table ───────────────────────────────────────────────────
function simpleTable(headers, rows) {
  const colW = Math.floor(9360 / headers.length);
  const colWidths = headers.map(() => colW);
  const headerRow = new TableRow({
    children: headers.map(h => new TableCell({
      borders: bdr(C.accent, 6),
      width: { size: colW, type: WidthType.DXA },
      margins: { top: 80, bottom: 80, left: 120, right: 120 },
      shading: { fill: C.tableHead, type: ShadingType.CLEAR },
      children: [new Paragraph({ children: [new TextRun({ text: h, bold: true, color: C.white, size: 20 })], spacing: { before: 0, after: 0 } })],
    })),
  });
  const dataRows = rows.map((row, ri) =>
    new TableRow({
      children: row.map(cell => new TableCell({
        borders: bdr(C.border, 4),
        width: { size: colW, type: WidthType.DXA },
        margins: { top: 80, bottom: 80, left: 120, right: 120 },
        shading: { fill: ri % 2 === 0 ? C.white : C.tableAlt, type: ShadingType.CLEAR },
        children: [new Paragraph({ children: [new TextRun({ text: String(cell), size: 20 })], spacing: { before: 0, after: 0 } })],
      })),
    })
  );
  return new Table({ width: { size: 9360, type: WidthType.DXA }, columnWidths: colWidths, rows: [headerRow, ...dataRows] });
}

function pageBreak() {
  return new Paragraph({ children: [new PageBreak()], spacing: { before: 0, after: 0 } });
}

